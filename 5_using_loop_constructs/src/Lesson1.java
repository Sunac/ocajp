/*
    Create and use while loops
 */
public class Lesson1 {

    public static void main(String[] args) {

        int i = 0;
        while(i < 5) {
            System.out.println(i);
            i++;
        }

        int j = 10;
        while(j > 0) {
            System.out.println(j);
            j--;
        }

    }
}
